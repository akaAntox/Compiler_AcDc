package token;

public enum TokenType {
    INT, // integer
    TYINT, // type int
    FLOAT, // float
    TYFLOAT, // type float
    ID, // identifier
    PLUS, // "+"
    MINUS, // "-"
    TIMES, // "*"
    DIV, // "/"
    ASSIGN, // "="
    PRINT, // "print"
    SEMI, // ";"
    EOF, // End of file
}